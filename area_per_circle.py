# This program calculates the area and perimeter of a circle
#   with a radius of 30 mm


import math


def main():
    print("For a circle with a radius")
    print("of 30 mm:")
    print("")
    print("Area = {} mm^2". format(math.pi*30**2))
    print("Circumference = {} mm". format(2*math.pi*30))


if __name__ == "__main__":
    main()
